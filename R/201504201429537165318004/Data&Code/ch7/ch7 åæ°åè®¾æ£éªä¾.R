var.test1<-function(x, sigma2){
  n<-length(x)
  S2=var(x)
  df=n-1
  chi2<-df*S2/sigma2;
  P<-pchisq(chi2,df)
  data.frame(var=S2, df=df, chisq2=chi2, P_value=P)
}
